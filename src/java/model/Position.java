package model;

/**
 *
 * @author Asus
 */
public class Position {
    private int posId;
    private String posName;

    public Position() {
    }

    public Position(int posId, String posName) {
        this.posId = posId;
        this.posName = posName;
    }

    public int getPosId() {
        return posId;
    }

    public void setPosId(int posId) {
        this.posId = posId;
    }

    public String getPosName() {
        return posName;
    }

    public void setPosName(String posName) {
        this.posName = posName;
    }
    
}
